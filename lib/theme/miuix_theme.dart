ThemeData buildMiuixLightTheme() {
  return ThemeData(
    useMaterial3: true,
    brightness: Brightness.light,
    colorScheme: const ColorScheme.light(
      primary: MiuixColors.primary,
      onPrimary: MiuixColors.onPrimary,
      error: MiuixColors.error,
      onError: MiuixColors.onError,
      secondary: MiuixColors.secondary,
      onSecondary: MiuixColors.onSecondary,
      surface: MiuixColors.surface,
      onSurface: MiuixColors.onSurface,
    ),
    scaffoldBackgroundColor: MiuixColors.surface,
    cardColor: MiuixColors.surfaceContainer,
    dividerColor: MiuixColors.dividerLine,
    appBarTheme: const AppBarTheme(
      backgroundColor: MiuixColors.surface,
      foregroundColor: MiuixColors.onSurface,
      elevation: 0,
      scrolledUnderElevation: 0,
      titleTextStyle: TextStyle(
        fontSize: 20,
        fontWeight: FontWeight.w500,
        color: MiuixColors.onSurface,
      ),
    ),
    cardTheme: CardThemeData(
      color: MiuixColors.surfaceContainer,
      elevation: 0,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(MiuixSpacing.cardCornerRadius),
      ),
      margin: EdgeInsets.zero,
    ),
    elevatedButtonTheme: ElevatedButtonThemeData(
      style: ElevatedButton.styleFrom(
        backgroundColor: MiuixColors.primary,
        foregroundColor: MiuixColors.onPrimary,
        minimumSize: const Size(MiuixSpacing.buttonMinWidth, MiuixSpacing.buttonMinHeight),
        padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 13),
        shape: RoundedRectangleBorder(
          borderRadius: BorderRadius.circular(MiuixSpacing.buttonCornerRadius),
        ),
        elevation: 0,
        textStyle: MiuixTextStyles.button,
      ),
    ),
    textButtonTheme: TextButtonThemeData(
      style: TextButton.styleFrom(
        foregroundColor: MiuixColors.primary,
        padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 13),
        shape: RoundedRectangleBorder(
          borderRadius: BorderRadius.circular(MiuixSpacing.buttonCornerRadius),
        ),
        textStyle: MiuixTextStyles.button,
      ),
    ),
    inputDecorationTheme: InputDecorationTheme(
      filled: true,
      fillColor: MiuixColors.secondaryContainer,
      contentPadding: const EdgeInsets.symmetric(horizontal: 16, vertical: 16),
      border: OutlineInputBorder(
        borderRadius: BorderRadius.circular(MiuixSpacing.textFieldCornerRadius),
        borderSide: BorderSide.none,
      ),
      enabledBorder: OutlineInputBorder(
        borderRadius: BorderRadius.circular(MiuixSpacing.textFieldCornerRadius),
        borderSide: BorderSide.none,
      ),
      focusedBorder: OutlineInputBorder(
        borderRadius: BorderRadius.circular(MiuixSpacing.textFieldCornerRadius),
        borderSide: const BorderSide(color: MiuixColors.primary, width: 2),
      ),
      errorBorder: OutlineInputBorder(
        borderRadius: BorderRadius.circular(MiuixSpacing.textFieldCornerRadius),
        borderSide: const BorderSide(color: MiuixColors.error),
      ),
      labelStyle: const TextStyle(color: MiuixColors.onSecondaryContainer, fontSize: 17),
      floatingLabelStyle: const TextStyle(color: MiuixColors.primary, fontSize: 10),
      hintStyle: const TextStyle(color: MiuixColors.onSecondaryContainer, fontSize: 17),
    ),
    dialogTheme: DialogThemeData(
      backgroundColor: MiuixColors.surfaceContainer,
      elevation: 0,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(MiuixSpacing.cardCornerRadius),
      ),
    ),
    bottomSheetTheme: const BottomSheetThemeData(
      backgroundColor: MiuixColors.surfaceContainer,
      elevation: 0,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.vertical(top: Radius.circular(MiuixSpacing.cardCornerRadius)),
      ),
    ),
    snackBarTheme: SnackBarThemeData(
      backgroundColor: MiuixColors.onSurface,
      contentTextStyle: const TextStyle(color: MiuixColors.surfaceContainer, fontSize: 14),
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(MiuixSpacing.cardCornerRadius),
      ),
      behavior: SnackBarBehavior.floating,
    ),
    chipTheme: ChipThemeData(
      backgroundColor: MiuixColors.secondaryContainer,
      selectedColor: MiuixColors.tertiaryContainer,
      labelStyle: MiuixTextStyles.body2,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(MiuixSpacing.cardCornerRadius),
      ),
    ),
    tabBarTheme: TabBarThemeData(
      labelColor: MiuixColors.primary,
      unselectedLabelColor: MiuixColors.onSurfaceVariantSummary,
      indicatorColor: MiuixColors.primary,
      labelStyle: MiuixTextStyles.body1.copyWith(fontWeight: FontWeight.w600),
      unselectedLabelStyle: MiuixTextStyles.body1,
    ),
    floatingActionButtonTheme: const FloatingActionButtonThemeData(
      backgroundColor: MiuixColors.primary,
      foregroundColor: MiuixColors.onPrimary,
      elevation: 0,
      shape: CircleBorder(),
    ),
    progressIndicatorTheme: const ProgressIndicatorThemeData(
      color: MiuixColors.primary,
      linearTrackColor: MiuixColors.secondaryContainer,
    ),
    switchTheme: SwitchThemeData(
      thumbColor: WidgetStateProperty.resolveWith((states) {
        if (states.contains(WidgetState.selected)) return MiuixColors.onPrimary;
        return MiuixColors.onSecondary;
      }),
      trackColor: WidgetStateProperty.resolveWith((states) {
        if (states.contains(WidgetState.selected)) return MiuixColors.primary;
        return MiuixColors.secondary;
      }),
    ),
  );
}

